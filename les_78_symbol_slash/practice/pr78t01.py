def is_right_tr(a, b, c, /, precision=0.001 ):
    if abs(c ** 2 - (a ** 2 + b ** 2)) < precision:
        return True
    elif abs(a ** 2 - (b ** 2 + c ** 2)) < precision:
        return True
    elif abs(b ** 2 - (a ** 2 + c ** 2)) < precision:
        return True
    
    return False


