# 1. Присваивание значения переменной: Walrus оператор позволяет присваивать значения переменным в одном операторе. Например: t := float(input()).

# 2. Простое присваивание: Walrus оператор может использоваться только для простого присваивания значения переменной. Например: a := 5, но a, b := 5, 6 не 
# является допустимым использованием walrus оператора.

# 3. Внутри цикла: Walrus оператор не может использоваться внутри заголовка цикла for. Например: for (t := x) in range(10): print(t) - это неверное использование.

# 4. Внутри условного оператора: Walrus оператор может использоваться внутри условного оператора if, но не внутри elif или else. Например: if (t := float(input())) > 0: print(t).

# 5. Порядок операторов: Walrus оператор должен быть использован перед оператором, который требует значения переменной. Например: print(a := 5, a := a + 1) - это 
# допустимое использование, но print(a := a + 1, a := 5) - нет.

# 6. Вывод значения переменной: Walrus оператор может использоваться для вывода значения переменной в одном операторе с другими операторами. Например: print(a := 5) выводит 
