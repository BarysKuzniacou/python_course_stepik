d = {0: 'безнадежно', 1: 'убого'}

print(*d) # 0 1

print({*d}) # {0, 1}

print({*d.values()}) # {'безнадежно', 'убого'}

print({*d.items()}) # {(1, 'убого'), (0, 'безнадежно')} 

print({**d}) # {0: 'безнадежно', 1: 'убого'} 

d2 = {2: 'неуд.', 3: 'удовл.'}

print({**d, **d2}) # {0: 'безнадежно', 1: 'убого', 2: 'неуд.', 3: 'удовл.'}