# функции. Если tp = 'list', то используется список, иначе (при другом значении) - кортеж. При этом 
# внешняя функция должна возвращать ссылку на вложенную функцию.

# Далее, на вход программы поступают две строки: первая - это значение для параметра tp; вторая - 
