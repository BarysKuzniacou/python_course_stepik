# Обратите внимание, что в возвращаемой коллекции должны присутствовать числа, а не их строковые представления.

def outer(tp):
    def inner(s):
        if tp == 'list':
            lst = list(map(int, s.split()))
            return lst

        return tuple(map(int, s.split()))
    
    return inner


tp_in = input()
nums = input()
p_func = outer(tp_in)
lst = p_func(nums)
